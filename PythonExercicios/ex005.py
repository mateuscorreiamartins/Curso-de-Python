# Faça um programa que leia um número inteiro e mostre 
# na tela o seu sucessor e seu antecessor.

n = int(input('Digite um número inteiro: '))
print('O sucessor desse número é: {}. O antecessor desse número é: {}.'.format(n+1, n-1))

i = int(input('Digite um número inteiro: '))
s = i+1
a = i-1
print('O sucessor desse número é: {}. O antecessor desse número é: {}.'.format(s, a))
